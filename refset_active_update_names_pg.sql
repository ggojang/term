-- 실행: psql -U postgres -d term -f refset_active_update_names_pg.sql

SET search_path TO term, public;

\echo 'Loading DESCRIPTION for REFSET_ID...'

UPDATE term.REFERENCESET_ACTIVE AS RA
SET REFSET_NAME = R2.D1T
FROM (
	SELECT D1.CONCEPT_ID AS D1CID, D1.TERM AS D1T
	FROM (
		SELECT REFSET_ID, EFFECTIVE_TIME
		FROM term.REFERENCESET_ACTIVE
		WHERE EFFECTIVE_TIME <= '20260601'
		GROUP BY REFSET_ID, EFFECTIVE_TIME
	) AS R1
	INNER JOIN term.DESCRIPTION AS D1
		ON D1.TYPE_ID = '900000000000003001'
		AND D1.CONCEPT_ID = R1.REFSET_ID
		AND D1.ACTIVE = 1
) AS R2
WHERE RA.REFSET_ID = R2.D1CID;

